          <h3>Image analysis</h3>
        <p>For help and information about image analysis please contact 
           <a href="people/Graeme.shtml">Graeme</a>.</p> 
        <p>We have a fully equipped image analysis suite in New Biochemistry.
           The workstations are integrated with our data storage system and 
           an <a href="http://www.openmicroscopy.org/site" title="OMERO website">
           OMERO image database server</a> (Biochemistry OMERO webclient
           <a href="http://omero1.bioch.ox.ac.uk/webclient" title="OMERO webclient">
           here</a> - internal only).</p>
        <img id="imsuite" src="images/image_suite.jpg"  width="200" height="133"
              alt="Micron image analysis suite" title="Micron image analysis suite">
        <p>The analysis workstations (a mix of Windows and Linux) are equipped with the following:-</p>
        <ul>
          <li><a href="http://fiji.sc/wiki/index.php/Fiji" title="Fiji website">Fiji</a>
            /<a href="http://rsbweb.nih.gov/ij/" title="ImageJ website">ImageJ</a>
            <ul class="sublist"><li>ImageJ is an excellent free image analysis tool supported by NIH (and
              Fiji is just ImageJ with batteries included!) ImageJ/Fiji are suitable for most image 
              analysis tasks, certainly in 2D (
              <a href="<?php wikiHref($extViewer,'/wiki/Fiji_/_ImageJ'); ?>" title="Fiji wiki page">
                view wiki page</a>).</li></ul></li>
          <li><a href="http://www.api.com/softworx-suite.asp" title="softWoRx website">SoftWoRx</a>
            <ul class="sublist"><li>for DeltaVision and OMX data; particularly wide-field deconvolution 
              and SI reconstruction (<a href="<?php wikiHref($extViewer,'SoftWoRx'); ?>" title="Volocity wiki page">view wiki page</a>).</li></ul></li>
          <li><a href="http://www.openmicroscopy.org/site/support/omero4/downloads#section-13" title="Omero website">OMERO client</a> 
            <ul class="sublist"><li>for accessing our OMERO image storage system (omero1.bioch.ox.ac.uk) to upload, annotate, process or download images (<a href="<?php wikiHref($extViewer,'OMERO'); ?>" title="OMERO wiki page">view wiki page</a>).</li></ul></li>
          <li><a href="http://www.perkinelmer.com/pages/020/cellularimaging/products/volocity.xhtml" title="Volocity website">Volocity</a> 
            <ul class="sublist"><li>for 3D image visualization and analysis (<a href="<?php wikiHref($extViewer,'Volocity'); ?>" title="Volocity wiki page">view wiki page</a>).</li></ul></li>
          <li><a href="http://www.mathworks.co.uk/products/matlab/" title="Matlab website">Matlab</a>
            <ul class="sublist"><li>for custom image processing and analysis solutions, as well as 
            tools such as <a href="http://emonet.biology.yale.edu/microbetracker/">MicrobeTracker</a> 
