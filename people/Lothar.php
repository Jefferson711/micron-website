        <h3>Lothar Schermelleh - Micron Group Leader</h3> email:<a href="mailto:lothar.schermelleh@bioch.ox.ac.uk">lothar.schermelleh@bioch.ox.ac.uk</a>
          <p><img src="../images/Lothar.jpg" alt="Lothar"
                         title="Lothar Schermelleh" width="135" height="180" >    
         </p>
         
        <p>Chromatin of higher eukaryotes is folded in a complex manner to form higher-order domains and fibres of variable compaction, demarcated by a network of largely chromatin-void interchromatin spaces and channels leading up to nuclear pores. Compartmentalization into restrictive and permissive local chromatin environments and their confinements to specific nuclear sites are thought to play a major role in the regulation of gene activity, mRNA transport, DNA replication and repair. This large-scale organization is determined by an intricate network of epigenetic factors (histone variants and modifications, DNA methylation, PcG proteins, non-translated RNAs), but also involves numerous structural proteins (e.g., Lamins, NUPs, HP1, Cohesins). </p>
        <p>&nbsp;</p>

<p>We apply and refine advanced optical imaging methods (e.g., super-resolution structured illumination, single molecule localization, FRAP, laser-microirradiation) to address general rules and dynamic aspects of higher order chromosomal organization in mammalian cell nuclei and its contribution to control genome function. We combine optimized immunolabelling, FISH and GFP-based protocols with customized statistical tools to quantitatively analyse spatial distribution pattern and dynamic properties of factors involved.  Comparative super-resolution 3D-mapping data from single cells in defined states are complementing population-wide ChIP-sequencing and chromosome conformation capturing approaches and will contribute to a better understanding of fundamental nuclear processes.</p>
<p>&nbsp; </p>

As an integral member of the Micron Advanced Bioimaging Unit, we also work on new correlative super-resolution approaches for live and fixed cell imaging that are expected to be of use for many collaborative research projects within the Oxford research community. 
