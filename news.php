        <h4>Micron Publication - <a href="http://pubs.acs.org/doi/full/10.1021/nl501870p">Kaufmann et al., Nano Letters. 2014.</a></h4>
        <img src="images/cryo2.png" alt="Rainer Graphical Abstract" width="350" align="right" />

        <p>
          Studying biological structures with fine details does not only
          require a microscope with high resolution, but also a sample
          preparation process that preserves the structures in a near-native
          state. Live-cell imaging is restricted mostly to the field of light
          microscopy. For studies requiring much higher resolution, fast
          freezing techniques (vitrification) are successfully used to
          immobilize the sample in a near-native state for imaging with
          electron and X-ray cryo-microscopy. Fluorescence cryo-microscopy
          combines imaging of vitrified samples with the advantages of
          fluorescence labeling of biological structures, but the advantages
          of vitrified specimens have not been fully exploited to date in
          fluorescence microscopy, because of the limited resolution of
          400-500 nm due to the lack of cryo-immersion objectives.
        </p>
        <p>
          We demonstrated how super-resolution imaging can be successfully
          applied for fluorescence cryo-microscopy to increase the resolution
          up to a factor of 5 at a temperature of -192Â°C. Using standard
          fluorescent proteins we were able to image biological structures
          in vitrified cells with a resolution of 125 nm and could locate
          individual molecules with an average precision of 40 nm. This
          technique is highly complementary to electron and X-ray
          cryo-microscopy and presents a powerful tool for imaging cellular
          structures in a near-native state at nanometer resolution.
        </p>

