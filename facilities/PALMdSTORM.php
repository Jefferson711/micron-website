        <h3>PALM-TIRF microscope</h3>
        <p>New Biochemistry Room 00-060 </p>
        <p>&nbsp;</p>
<p>This microscope was custom-built by the Kapanidis lab in Physics. It can be used for PALM and dSTORM experiments under TIRF or near-TIRF conditions up to 1 Âµm into the cell. Both methods are used to localise single molecules beyond the diffraction limit. TIRF and near-TIRF localisation is limited to 2D with no z-information. Localisation accuracy depends on a high number of detected photons and on only a fraction of molecules being in a fluorescent state at any one time. The latter is achieved in PALM by the activation of only a few fluorophores at the same time, which then bleach irreversibly, and in dSTORM by reversible switching between a fluorescent on-state and a dark off-state. PALM uses photoactivatable fluorescent proteins and dSTORM images fluorescent organic dyes. Cells need to be in an aqueous medium and for dSTORM need to be imaged in switching buffer. </p>
