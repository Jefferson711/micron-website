        </p>
        <dl>
          <dt>
            <a href="http://fiji.sc/wiki/index.php/Fiji" title="Fiji website">Fiji</a>
            /<a href="http://rsbweb.nih.gov/ij/" title="ImageJ website">ImageJ</a>
          </dt>
          <dd>
            ImageJ is an excellent free image analysis tool supported by NIH
            (and Fiji is just ImageJ with batteries included for the life
            sciences) ImageJ/Fiji are suitable for most image analysis tasks,
            certainly in 2D.
          </dd>
          <dt>
            <a href="http://www.api.com/softworx-suite.asp" title="softWoRx website">SoftWoRx</a>
          </dt>
          <dd>
            for DeltaVision and OMX data; particularly wide-field deconvolution
            and SI reconstruction
          </dd>
          <dt>
            <a href="http://www.openmicroscopy.org/site/support/omero5/downloads" title="Omero website">OMERO client</a>
          </dt>
          <dd>
            for accessing our OMERO image storage system (omero1.bioch.ox.ac.uk)
            to upload, annotate, process or download images.
          </dd>
          <dt>
            <a href="http://www.bitplane.com/go/products/imaris" title="Imaris website">Imaris</a>
          </dt>
          <dd>
            for 3D visualization and analysis, particularly 3D segmentation
            and movie-making.
          </dd>
          <dt>
            <a href="http://www.perkinelmer.com/pages/020/cellularimaging/products/volocity.xhtml" title="Volocity website">Volocity</a>
          </dt>
          <dd>
            for 3D visualization and analysis, particularly spinning disk
            confocal datasets.
          </dd>
          <dt>
            <a href="http://www.mathworks.co.uk/products/matlab/" title="Matlab website">Matlab</a>
          </dt>
          <dd>
            for custom image processing and analysis solutions, as well as
            tools such as <a href="http://emonet.biology.yale.edu/microbetracker/">MicrobeTracker</a>.
          </dd>
          <dt>
            <a href="http://msg.ucsf.edu/IVE/">Priism</a> &amp; <a href="http://code.google.com/p/priithon/">Prithon</a>
          </dt>
          <dd>
            free image analysis tools from the Agard &amp; Sedat labs, where
            the Deltavision &amp; and OMX technologies originated.
          </dd>
          <dt>
            <a href="http://inkscape.org/" title="Inkscape website">Inkscape</a>
          </dt>
          <dd>
            for figure editing.
          </dd>
          <dt>
            <a href="http://www.gimp.org/" title="Gimp website">Gimp</a>
          </dt>
          <dd>
            image editor, also for figure editing
          </dd>
        </dl>
