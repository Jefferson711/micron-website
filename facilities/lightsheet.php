        <h3>Zeiss Z1 Lightsheet microscope</h3>
        <p>New Biochemistry Room 00-060 </p>
        <p>&nbsp;</p>
<p>This is a commercial lightsheet microscope designed primarily for time-lapse imaging of thick specimens that a scanning confocal microscope would struggle with. Imaging of thick, fixed samples is also possible. Specimens are illuminated with one or two thin light sheets from opposite sides perpendicular to the detection objective. This vastly reduces light exposure and therefore bleaching of the sample and enables penetration depths of up to 100 &micro;m. It is possible to image in single or multiview mode, the latter through rotating the sample and imaging stacks at six to eight different angles, which can then be recombined into a 360&deg; view. Specimens are embedded in 1% low-melting agarose or similar inside glass capillaries and then extruded and imaged in buffer. </p>
<p>&nbsp;</p>
This system is fitted with an incubation chamber with temperature and CO2 control.
           
