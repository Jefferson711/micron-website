        <h3>UltraView Spinning Disk system (Barr)</h3>
        <p>New Biochemistry Room 00-056 </p>
        <p>&nbsp;</p>
<p>The UltraView system is a spinning disc confocal, providing optical sectioning while still permitting rapid imaging, on the order of 20 frames/s. It has 6 laser lines, 405, 488, 514, 532, 568 and 633. This allows a very wide range of fluorescent probes to be used on the system. The instrument is also fitted with a photo-bleach/activation unit, which can be used to provide either single spots or arbitrary shapes via line or raster scans. This instrument is particularly useful for doing live cell imaging as the high imaging speed and low background provide crisp images of even fast moving objects in live cells.</p>
