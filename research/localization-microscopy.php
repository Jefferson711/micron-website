      <h3>Localization Microscopy</h3>

      <p> Localization microscopy is a technique of super-resolution fluorescence microscopy which is based on the very precise determination of the positions of single fluorescent molecules in a sample. This enables a structural resolution in biological sample down to the range of 20 nm using light in the visible spectrum.</p>

      <img src="../images/localization_microscopy.jpg"
                    width="579" height="235" alt="localization microscopy" title="localization microscopy">     

      <p>In first approaches of localization microscopy (PALM, FPALM, STORM) special photoactivatable or photoswitchable fluorophores where used to achieve optical isolation of the single molecule signals via switching the fluorophores between a "bright" and a "dark" state. Recently, methods like dSTORM, SPDM and GSDIM have been developed for using standard fluorophores (e.g. Alexa or Atto dyes or fluorescent proteins). A light induced long-lived dark state of the fluorescent molecules and the stochastic recovery to the fluorescent state allows an optical isolation of the single molecule signals in a very similar way as in the approaches using special activatable or switchable dyes.</p>

<p>The positions of the fluorophores can be determined very precisely by fitting a model function to the detected single molecule signals. Typically several thousands to millions of molecule positions are required for the reconstruction of a super-resolution image. Therefore, data stacks consisting of several thousand frames are recorded within a timeframe of seconds up to minutes. The structural resolution in the final image is determined by the density of molecule positions as well as the localisation accuracy.</p>

<p>Besides a structural resolution, that is up to 10 times better than in conventional fluorescence microscopy, localization microscopy provides additional single molecule information which can be used for further statistical analysis of the data. This allows for example determining of local densities or dynamic information about the detected proteins within the very highly resolved structures.
First approaches have shown the applicability of localization microscopy for live cell imaging with a temporal resolution of up to 500 ms.</p>
  
